/**
 * 创建SVG元素
 */
export function createIcon(iconName: keyof typeof icons, className?: string): SVGElement {
  const iconSvg = icons[iconName]
  if (!iconSvg) {
    throw new Error(`Icon "${iconName}" not found`)
  }

  const div = document.createElement('div')
  div.innerHTML = iconSvg.trim()
  const svg = div.firstElementChild as SVGElement

  if (className) {
    svg.classList.add(className)
  }

  return svg
}

/**
 * 获取图标HTML字符串
 */
export function getIconHTML(iconName: keyof typeof icons): string {
  const iconSvg = icons[iconName]
  if (!iconSvg) {
    throw new Error(`Icon "${iconName}" not found`)
  }
  return iconSvg.trim()
}
